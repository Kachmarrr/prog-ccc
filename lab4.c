#include <stdio.h>

int main() {
    int day;

    // Запитуємо номер дня тижня від користувача
    printf("Введіть номер дня тижня (1-7): ");
    scanf("%d", &day);

    // Перевіряємо, який день тижня ввів користувач та виводимо відповідне повідомлення
    if (day >= 1 && day <= 5) {
        printf("Робочий день\n");
    } else {
        printf("Не робочий день\n");
    }

    return 0;
}
